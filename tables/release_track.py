
class ReleaseTrack:
    def __init__(self, release_id=None, sequence=None, position=None, parent=None, title=None, duration=None, track_id=None):
        self.release_id = release_id
        self.sequence = sequence
        self.position = position
        self.parent = parent
        self.title = title
        self.duration = duration
        self.track_id = track_id